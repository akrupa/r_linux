# HELP
git branch -h

# Create branch
git branch <name_of_new branch> # clone branch we are in daring using this command
git branch <name_of_new branch>  HEAD  # same above

# Create branch from  pointed branch
git branch <name_of_new branch> <source_of_new_branch>

# show all local branch
git branch
# show all remotes branch
git branch -r, --remotes
# show ALL branch
git branch -a


